package org.egov.collection.bean;

public class WardNumber {
	
    private String wardNoCode;
	
	private String wardNoName;

	public String getWardNoCode() {
		return wardNoCode;
	}

	public void setWardNoCode(String wardNoCode) {
		this.wardNoCode = wardNoCode;
	}

	public String getWardNoName() {
		return wardNoName;
	}

	public void setWardNoName(String wardNoName) {
		this.wardNoName = wardNoName;
	}
	
	

}
